#Tipo Entero o largo
numero = 15
print(numero,type(numero))

#Tipo Flotante
numero_flotante = 15.105
print(numero_flotante,type(numero_flotante))

#Tipo Complejo
numero_complejo=5+6j
print(numero_complejo,type(numero_complejo))

#Tipo String
nombre="Cadena"
print(nombre,type(nombre))

#Tipo Booleano
V_F=2==3
print(V_F,type(V_F))
